func readSPCMessage(filePath string) []byte {
	f, err := os.Open(filePath)
	defer f.Close()
	checkErr(err)

	spcMessage, err := ioutil.ReadAll(f)
	checkErr(err)

	return spcMessage

}
func readCKCMessage(filePath string) []byte {
	f, err := os.Open(filePath)
	defer f.Close()
	checkErr(err)

	ckcMessage, err := ioutil.ReadAll(f)
	checkErr(err)

	return ckcMessage

}

func checkErr(err error) {
	if err != nil {
		panic(err)
	}
}
